-- ============================================================================
-- FIX — options немаше `created_at` (или било која ordering колона), а
-- поправката за update-in-place на опции при уредување анкета (фикс #9)
-- зависи од стабилен, хронолошки редослед за да ги спарува постоечките
-- опции со новите вредности по позиција. UUID id-та се случајни (не
-- секвенцијални), па нема имплицитен редослед без ова.
-- ============================================================================

ALTER TABLE public.options ADD COLUMN IF NOT EXISTS created_at TIMESTAMPTZ DEFAULT NOW();

-- Backfill: без вистински историски timestamp, само стабилен редослед по id
-- е доволен за постоечките редови (се користи еднаш, за нови редови NOW() е точен).
UPDATE public.options SET created_at = NOW() WHERE created_at IS NULL;
